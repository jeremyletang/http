open Printf
open OUnit
open List

let run_test_suite = function
  | TestLabel (name, tests) ->
    printf "Running tests for %s\n" name;
    ignore(run_test_tt_main tests)
  | _ -> printf "Unknown test suite";
;;

let _ =
  List.iter run_test_suite [Header_test.suite;
                            Method_test.suite;
                            Parser_test.suite;
                            Status_test.suite;
                            Version_test.suite;]
;;